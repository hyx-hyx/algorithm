.486
DATA SEGMENT USE16
INPUTBUF DB 255
         DB ?
         DB 255 DUP(?)
LL DB 0
SUM DB 0
DATA ENDS
CODE SEGMENT USE16
    ASSUME CS:CODE,DS:DATA
BEG:
    MOV AX,DATA
    MOV DS,AX
    CALL INPUT
    CALL COMPARE
    CALL NEWLINE
    CALL BINARY
    MOV AH,4CH
    INT 21H

INPUT PROC
    MOV AH,0AH
    MOV DX,OFFSET INPUTBUF
    INT 21H
    RET
INPUT ENDP
    
COMPARE PROC
    MOV BX,OFFSET INPUTBUF
    ADD BX,2
    MOV CL,[INPUTBUF+1]
    MOV CH,0
LESS:
    CMP BYTE PTR [BX],42H
    JC NEXT
GREATER:
    CMP BYTE PTR [BX],46H
    JNC NEXT
    INC SUM
NEXT:
    INC BX
    LOOP LESS
    RET
COMPARE ENDP

BINARY PROC
    MOV CX,8
    MOV BL,SUM
TESTBIT:
    RCL BL,1
    JC ONE 
    MOV AH,02H
    MOV DL,30H
    INT 21H
    LOOP TESTBIT
    RET
ONE:
    MOV AH,02H
    MOV DL,31H
    INT 21H 
    LOOP TESTBIT
    RET
BINARY ENDP
NEWLINE PROC
    MOV AH,02H
    MOV DL,0DH
    INT 21H
    MOV AH,02H
    MOV DL,0AH
    INT 21H
    RET
NEWLINE ENDP
CODE ENDS
    END BEG